package j04_입력.복습;

import java.util.Scanner;

public class Input {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		
		scanner.next(); //문자열
		scanner.nextInt(); //정수
		scanner.nextDouble(); //실수
		scanner.nextBoolean(); //논리
		scanner.nextLine(); //문자열	띄어쓰기, 엔터
		
	}

}
