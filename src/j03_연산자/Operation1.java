package j03_연산자;

public class Operation1 {

	public static void main(String[] args) {
		/*
		 * int num = 10;
		 * num <= 11;
		 */
		
		// 변수 선언 -> 변수 선언은 최초에 딱 한번만 한다.
		int num;
		
		// 초기화 -> 변수 안의 값을 비우고 다른 값으로 대체하는 것.
		num = 10;
		System.out.println(num);
		num = num + 1;
		System.out.println(num);
		System.out.println(++num);
		System.out.println(num);
		
		
		
		 

	}

}
